//Some basic math functions
console.log(Math); //Shows it's an obj datatype
console.log(Math.abs(-15)); //Returns modulus value
console.log(Math.round(3.6)); //Returns rounded value
console.log(Math.ceil(5.1));  //Returns ceiling value
console.log(Math.floor(5.9));  //Returns floor value
console.log(Math.max(5,9,4,3,7)); //max value
console.log(Math.min(5,9,4,3,7)); //min value



